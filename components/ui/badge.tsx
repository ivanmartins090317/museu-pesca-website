import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-3 py-1 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default:
          "border-transparent bg-primary-ocean text-neutral-white hover:bg-primary-ocean/80",
        secondary:
          "border-transparent bg-primary-aqua text-neutral-white hover:bg-primary-aqua/80",
        outline: "text-primary-ocean border-primary-ocean",
        info: "border-transparent bg-primary-stone text-neutral-white",
        highlight:
          "border-transparent bg-primary-aqua text-neutral-white font-bold",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  );
}

export { Badge, badgeVariants };

